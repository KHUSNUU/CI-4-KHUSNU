                 Sejarah:

                        PT.  KHUSNU TRANS  didirikan pada tahun 2026 oleh H. M. KHUSNU MAROM asal Demak, Jawa Tengah.

                        Dengan mendapatkan pinjaman dari bank, ia membeli enam buah bus dan menggunakan armadanya tersebut untuk rute Semarang-Jakarta.

                        Tetapi setelah beberapa waktu, rute tersebut dianggap kurang menguntungkan dikarenakan sepinya penumpang. Akhirnya ia mengubah armada tersebut menjadi kelas eksekutif dan mengalihkan trayeknya ke trayek antarkota antarprovinsi Jakarta-Kudus, Jakarta-Pati dan Jakarta-Jepara.

                        Mulai saat itulah perusahaan otobusnya mulai berkembang. Pada tahun 2009, PO Haryanto melakukan ekspansi bisnis pertamanya diluar Muria Raya, yakni di Madura dengan trayek Jakarta-Pamekasan-Sumenep.

                        Tiga tahun kemudian tepatnya di tahun 2012, PT. KHUSNU TRANS kembali melakukan ekspansi bisnisnya, kali ini berada di jalur selatan jawa dengan trayek pertama yakni Jakarta-Solo-Ponorogo, serta kota-kota lain di sekitar Solo Raya seperti Klaten dan Gemolong.

                        Di tahun yang sama pula, PT KHUSNU TRANS juga merintis trayek menuju Bojonegoro dan Purwodadi dengan bantuan adiknya, H. Annas. Saat ini, PT. KHUSNU TRANS telah melayani lebih dari 20 kota di Pulau Jawa dengan beberapa divisi.[1]

                        Pada Juni 2022, PT. KHUSNU TRANS memberhentikan Rian Mahendra sebagai Direktur Operasional, karena alasan yang belum dapat diketahui. Rian bergabung mengembangkan usaha milik ayahnya tersebut sejak tahun 2003 bersama adik-adiknya, langsung di bawah supervisi ayahnya H. M.KHUSNU MAROM.

